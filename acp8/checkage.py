# Take input of age from the user
age = int(input("Enter your age: "))

# Check whether the age is between 10 and 20
if age >= 10:
    if age <= 20:
        print("Age is between 10 and 20 years")
    else:
        print("Age is not between 10 and 20 years")
else:
    print("Age is not between 10 and 20 years")
